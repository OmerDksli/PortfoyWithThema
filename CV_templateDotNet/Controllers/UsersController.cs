using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using CV_templateDotNet.Data;
using CV_templateDotNet.Models;
using Microsoft.AspNetCore.Identity;

namespace CV_templateDotNet.Controllers
{
    public class UsersController : Controller
    {
        private readonly CVtemplateDotNetContext _context;

        public UsersController(CVtemplateDotNetContext context)
        {
            _context = context;
        }

        // GET: Users
        public async Task<IActionResult> Index()
        {
              return _context.User != null ? 
                          View(await _context.User.Include(a=>a.ProfilImage).ToListAsync()) :
                          Problem("Entity set 'CVtemplateDotNetContext.User'  is null.");
        }

        // GET: Users/Details/5
        public async Task<IActionResult> Details(int? id)
        {
            if (id == null || _context.User == null)
            {
                return NotFound();
            }

            var user = await _context.User
                .FirstOrDefaultAsync(m => m.Id == id);
            if (user == null)
            {
                return NotFound();
            }

            return View(user);
        }

        // GET: Users/Create
        public IActionResult Create()
        {
            return View();
        }

        // POST: Users/Create
        // To protect from overposting attacks, enable the specific properties you want to bind to.
        // For more details, see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Create(User user)
        {
            if (ModelState.IsValid)
            {
                if (user.Image != null)
                {
                    foreach (var item in user.Image)
                    {
                        #region Bu dosya içerisine gelen resim dosyası işlemlerden geçerek kaydedilir

                        string imageExtension = Path.GetExtension(item.FileName);
                        string imageName = Guid.NewGuid() + imageExtension;
                        string path = Path.Combine(Directory.GetCurrentDirectory(), $"wwwroot/images/{imageName}");
                        using var stream = new FileStream(path, FileMode.Create);
                        await item.CopyToAsync(stream);
                        #endregion
                        /*CvImagePath properity sine resmin kayıt yolu path üzerinden kayedilmedi çünkü
                         * Kendi cihazımda resim eklerken bu cihazda ki resim dosya yolu kayıt edilir 
                         * ve kullanma esnasında sorun yaşanırdı. Böylece dosya yolu cihazdan bağımsız Olarak 
                         * sadece tilda işareti ile wwwRoot içerisinden Kullanılabilir 
                        */
                        ImagePath imageWay = new() { CvImagePath = $"images/{imageName}", User = user };
                        await _context.AddAsync(imageWay);
                    }
                }

                _context.Add(user);
                await _context.SaveChangesAsync();
                return RedirectToAction(nameof(Index));
            }
            return View(user);
        }

        // GET: Users/Edit/5
        public async Task<IActionResult> Edit(int? id)
        {
            if (id == null || _context.User == null)
            {
                return NotFound();
            }

            var user = await _context.User.FindAsync(id);
            if (user == null)
            {
                return NotFound();
            }
            return View(user);
        }

        // POST: Users/Edit/5
        // To protect from overposting attacks, enable the specific properties you want to bind to.
        // For more details, see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Edit(int id,User user)
        {
            if (id != user.Id)
            {
                return NotFound();
            }

            if (ModelState.IsValid)
            {
                try
                {
                    _context.Update(user);
                    await _context.SaveChangesAsync();
                }
                catch (DbUpdateConcurrencyException)
                {
                    if (!UserExists(user.Id))
                    {
                        return NotFound();
                    }
                    else
                    {
                        throw;
                    }
                }
                return RedirectToAction(nameof(Index));
            }
            return View(user);
        }

        // GET: Users/Delete/5
        public async Task<IActionResult> Delete(int? id)
        {
            if (id == null || _context.User == null)
            {
                return NotFound();
            }

            var user = await _context.User
                .FirstOrDefaultAsync(m => m.Id == id);
            if (user == null)
            {
                return NotFound();
            }

            return View(user);
        }

        // POST: Users/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> DeleteConfirmed(int id)
        {
            if (_context.User == null)
            {
                return Problem("Entity set 'CVtemplateDotNetContext.User'  is null.");
            }
            var user = await _context.User.FindAsync(id);
            if (user != null)
            {
                _context.User.Remove(user);
            }
            
            await _context.SaveChangesAsync();
            return RedirectToAction(nameof(Index));
        }

        private bool UserExists(int id)
        {
          return (_context.User?.Any(e => e.Id == id)).GetValueOrDefault();
        }
    }
}
